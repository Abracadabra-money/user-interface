<template>
  <footer class="app-footer" :class="{ transparent: itsTransparent }">
    <div class="container">
      <div class="leftLinks">
        <SocialLink
            v-for="(item, index) in filteredLeftIcons"
            :key="index"
            :data="item.data"
            :link="item.link"
            :icon="item.icon"
            :text="item.text"
        />
      </div>
      <div class="rightLinks">
        <SocialLink
            v-for="(item, index) in filteredRightIcons"
            :key="index"
            :data="item.data"
            :link="item.link"
            :icon="item.icon"
            :text="item.text"
        />
      </div>
    </div>
  </footer>
</template>

<script>

const SocialLink = () => import("@/components/SocialLink");
import Discord from "../assets/images/discord.svg";
import Twitter from "../assets/images/twitter.svg";
import Medium from "../assets/images/medium.svg";
import Content from "./UiComponents/Content.vue";
import IconContent from "./UiComponents/IconContent.vue";

export default {
  data() {
    return {
      socialLink: [
        {
          data: Content,
          text: "GitBook",
          link: "https://gitbook.com",
          position: "left",
        },
        {
          data: IconContent,
          icon: Discord,
          link: "https://discord.com",
          position: "right",
        },
        {
          data: IconContent,
          icon: Twitter,
          link: "https://twitter.com",
          position: "right",
        },
        {
          data: IconContent,
          icon: Medium,
          link: "https://medium.com",
          position: "right",
        },
      ],
      filteredLeftIcons: [],
      filteredRightIcons: [],
    };
  },
  components: {
    SocialLink,
  },
  mounted() {
    this.socialLink.forEach((item) => {
      if (item.position === "left") {
        this.filteredLeftIcons.push(item);
      } else if (item.position === "right") {
        this.filteredRightIcons.push(item);
      }
    });
  },
  computed: {
    itsTransparent() {
      const pages = ["Home", "Docs", "Tech", "Liquidations"];
      return pages.indexOf(this.$route.name) !== -1;
    },
  }
};
</script>
